let a = 10;
let b = 5;

// Arithmetic Operators:-
console.log(a+b);
console.log(a-b);
console.log(a*b);
console.log(a/b);
console.log(a%b);
console.log(a**b);

// Unary Operators:-
console.log(a++); //10
console.log(++a); //12

// Assignement Operators:-
let c = b;
console.log(c);

// Comparision Operators:-
let age = 18;
console.log(age < 23);

let n = 5;
let str = '5';

console.log(5 != 5);
console.log(n == str); // compare value not type
console.log(n === str); // compare value and type
